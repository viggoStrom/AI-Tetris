
class piece {
    constructor(pos, grids) {
        this.x = pos[0]
        this.y = pos[1]
        this.rotationIndex = 0
        this.grids = grids
    }

    currentGrid() {
        return this.grids[this.rotationIndex]
    }

    rotateLeft() {
        if (this.rotationIndex > 0) {
            this.rotationIndex--
        } else {
            this.rotationIndex = 3
        }
    }

    rotateRight() {
        if (this.rotationIndex < 3) {
            this.rotationIndex++
        } else {
            this.rotationIndex = 0
        }
    }
}

